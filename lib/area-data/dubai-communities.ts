export interface DubaiCommunity {
  code: string
  name: string
  sector: number
  sectorName: string
  area_km2: number
  population: number
  slug: string
}

const SECTOR_NAMES: Record<number, string> = {
  1: 'Deira & Old Dubai',
  2: 'North Dubai',
  3: 'Central & West Dubai',
  4: 'North Central',
  5: 'Southwest (Jebel Ali)',
  6: 'New Dubai',
  7: 'Northeast',
  8: 'East (Hatta Region)',
  9: 'South Dubai',
}

function toSlug(name: string): string {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '')
}

function c(code: string, name: string, sector: number, area_km2: number, population: number): DubaiCommunity {
  return { code, name, sector, sectorName: SECTOR_NAMES[sector], area_km2, population, slug: toSlug(name) }
}

