/**
 * Returns a description for a community by name.
 * Tries exact lowercase match, then slug-normalised match.
 * Returns null if not found.
 */
export function getAreaDescription(areaNameEn: string): string | null {
  const lower = areaNameEn.toLowerCase()
  if (DESCRIPTIONS[lower]) return DESCRIPTIONS[lower]
  // Normalise: collapse non-alpha to single space
  const norm = lower.replace(/[^a-z0-9]+/g, ' ').trim()
  return DESCRIPTIONS[norm] ?? null
}
